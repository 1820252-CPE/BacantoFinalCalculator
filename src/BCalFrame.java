    private void screenDisplayKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_screenDisplayKeyTyped
        // TODO add your handling code here:
          char c=evt.getKeyChar();
         if(!(Character.isDigit(c)||(c==KeyEvent.VK_BACK_SPACE)||(c==KeyEvent.VK_DELETE))){
        getToolkit().beep();
        evt.consume();
    }
    }//GEN-LAST:event_screenDisplayKeyTyped

