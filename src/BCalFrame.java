    private void btnEqualOpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualOpActionPerformed
        // Equal Operation Button
        String answer;
        secondNum = Double.parseDouble(screenDisplay.getText());
        
        if (operation == "+"){
            result = firstNum + secondNum;
            answer = String.format("%.2f", result);
            screenDisplay.setText(answer);
        }else if (operation == "-"){
            result = firstNum - secondNum;
            answer = String.format("%.2f", result);
            screenDisplay.setText(answer);
        }else if (operation == "/"){
            result = firstNum / secondNum;
            answer = String.format("%.2f", result);
            screenDisplay.setText(answer);
        }else if (operation == "*"){
            result = firstNum * secondNum;
            answer = String.format("%.2f", result);
            screenDisplay.setText(answer);
        }
    }//GEN-LAST:event_btnEqualOpActionPerformed

